public class S02_ejr_01 {
  public static void main( String[] args) {
    int y = 144;
    int x = 999;

    System.out.println("\nMostrando operaciones: \n");
    System.out.println(x);
    System.out.println(y);
    System.out.println("La suma  es " + (x + y));
    System.out.println("La resta es " + (x - y));
    System.out.println("La mult  es " + (x * y));
    System.out.println("La div   es " + (x / y));
  }
}
